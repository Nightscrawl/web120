    <h2>Shopping Carts</h2>

    <p>
        Ecommerce is booming and shows no signs of slowing down. While behemoth Amazon accounts for over 40% of U.S. ecommerce sales, “[t]he desire for instant access, round-the-clock accessibility and fast turnaround” has spurred growth across the entire internet (Hatch). Not only are consumers increasingly turning to the online portals of traditional brick-and-mortar stores such as Barnes and Noble and Walmart, but internet-only storefronts for smaller merchants provided by Amazon and Etsy allow smaller fish to swim in a larger pond without being consumed. Despite these trends, “46% of American small businesses do not have a website. (Hatch)”
    </p>

    <p>
        The small business owner has two options: partner with a parent company like Amazon that will do all of the heavy digital lifting, or remain independent by exploring the various options that will turn a static informational website into an online store.
    </p>

    <p>
        Should a merchant want to remain independent, a solution is provided in the form of <strong>shopping cart software</strong>. This ecommerce platform can connect to a new or existing website and will provide the foundation to build a digital storefront, allow secure payment, and enable the owner to customize and administer the business (3dCart). 
    </p>

    <h3>What are the options?</h3>

    <p>
        Open-source software is free but comes with all the pros <em>and cons</em> of open-source software in general: no outlay expenditure, bug fixes, customization, lack of official support, self-installation and administration.
    </p>

    <p>
        Licensed software is a one-time fee or subscription-based model that brings similar issues as its open-source competition, but with more official support. 
    </p>

    <p>
        Hosted ecommerce platforms have a third party manage and control all of the technical aspects including the software and server hosting.
    </p>

    <p>
        The choice depends on the needs and resources of the owner(s) and the business. Open-source software should only be selected if the business owner has technical experience or is able to hire a team that does. It might just be that any savings gained from making the initial choice is rendered moot, or worse, rendered a money sink, should problems arise that need continual fixing. Licensed software also needs administration and maintenance by a tech-savvy team. Hosted ecommerce platforms are “the most hassle-free way of getting an [ec]ommerce shopping cart for your website,” favoring ease of use, expedience, and the benefit of allowing the owner(s) to focus on the day-to-day aspects of the business, rather than the technicalities of their digital storefront (3dCart).
    </p>

    <h3>Where do I find them?</h3>

    <p>
        Paypal, the worldwide online payment platform, <a href="https://www.paypal.com/us/webapps/mpp/shopping-cart" target="_blank">partners with several shopping cart and ecommerce vendors</a>, combining its reliable, secure, and convenient payment options with the storefront of the business owner’s choosing. There are also a variety of options for varying business types, from a full-featured enterprise solution to a Wordpress shopping cart plugin.
    </p>

    <p>
        In September 2019, PC Magazine published their list of the top ten ecommerce software and platforms. Tied for first place along with PinnacleCart, Shopify is praised as “the go-to choice for [e]commerce platforms” for providing the tools needed at launch in order to get a digital storefront up and running (Martinez).
    </p>

    <p>
        While brick-and-mortar stores are holding their own, the internet’s share of the retail market is growing with each passing year (Ward). Should a business, large or small, want to take advantage of this opportunity, there are a variety of shopping cart and ecommerce platforms from which to choose. 
    </p> 

