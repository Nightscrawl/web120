    <h2>Galleries</h2>

    <p>
        The posting, display, and sharing of images has been a feature of the internet since its inception (Estes). Whether a company is seeking to display its wares on their digital storefront, an artist wants to promote her work, or an individual is looking to share memories with friends and family, a popular way to show off images of all types is the image gallery.
    </p>

    <p>
        An image gallery may be static, dynamic, interactive, a combination or all of these. As of this writing, a Google search for “image gallery” returns over 300 million results: the first two are informational; nearly every one on the first page relates to implementing an image gallery on a web site (Google).
    </p>

    <p>
        With HTML5, CSS3, JavaScript, and various frameworks, there are many options with which to create galleries of all sorts, meeting any need or purpose.
    </p>

    <h3>Static Galleries</h3>

    <p>
        A static gallery is one that is akin to a collage of images; the viewer is meant to take in the whole, rather than appreciate each individually.
    </p>

    <h3>Dynamic Galleries</h3>

    <p>
        Usually called a “slideshow,” dynamic galleries are also about visual presentation, as they are typically an automatic rotating selection of images. These are commonly seen in the header element of a web page, sometimes accompanied by a caption or a link to a page related to the image.
    </p>

    <h3>Interactive Galleries</h3>

    <p>
        Interactive galleries combine visual presentation elements with a focus on individual images, allowing the user to interact with the gallery itself to select the image she wants to examine.
    </p>       

